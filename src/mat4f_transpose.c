#include "mat4f.h"

t_mat4f	mat4f_transpose(t_mat4f m)
{
	t_mat4f	t;
	int		i;
	int		j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			t.m[i][j] = m.m[j][i];
			j++;
		}
		i++;
	}
	return (t);
}
