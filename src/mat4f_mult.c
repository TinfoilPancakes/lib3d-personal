#include "mat4f.h"

t_mat4f	mat4f_mult(t_mat4f l, t_mat4f r)
{
	t_mat4f	m;
	int		i;
	int		j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			m.m[i][j] = l.m[i][0] * r.m[0][j];
			m.m[i][j] += l.m[i][1] * r.m[1][j];
			m.m[i][j] += l.m[i][2] * r.m[2][j];
			m.m[i][j] += l.m[i][3] * r.m[3][j];
			j++;
		}
		i++;
	}
	return (m);
}
