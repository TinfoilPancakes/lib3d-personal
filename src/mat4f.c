#include "mat4f.h"

t_mat4f	mat4f(void)
{
	t_mat4f	q;
	int		i;
	int		j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (i == j)
				q.m[i][j] = 1;
			else
				q.m[i][j] = 0;
			j++;
		}
		i++;
	}
	return (q);
}
