#include "vec3f.h"

t_vec3f	vec3f_cross(t_vec3f l, t_vec3f r)
{
	t_vec3f	v;

	v.x = l.y * r.z - l.z * r.y;
	v.y = l.z * r.x - l.x * r.z;
	v.z = l.x * r.y - l.y * r.x;
	return (v);
}
